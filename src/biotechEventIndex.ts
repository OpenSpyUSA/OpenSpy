import type { SourceLink } from './types'

export type BiotechProceedingType = 'Administrative' | 'Congress'

export interface BiotechProceeding {
  date: string
  id: string
  source: SourceLink
  sourceLinks?: SourceLink[]
  title: string
  topics: string[]
  type: BiotechProceedingType
  venue: string
}

const sourceDateFormatter = new Intl.DateTimeFormat('en-US', {
  day: 'numeric',
  month: 'short',
  timeZone: 'UTC',
  year: 'numeric',
})

function makeSource(
  date: string,
  label: string,
  url: string,
  searchText: string,
  extras: Partial<SourceLink> = {},
): SourceLink {
  return {
    dateLabel: sourceDateFormatter.format(new Date(`${date}T00:00:00Z`)),
    label,
    searchText,
    url,
    ...extras,
  }
}

